/**
 * BX24.init(callback) — инициализация SDK Битрикс24.
 * Callback вызывается, когда:
 *   1. Библиотека bx24.js загружена и распарсена,
 *   2. Токены аутентификации получены от родительского окна Битрикс24,
 *   3. iframe встроен и готов к API-вызовам.
 * Все вызовы BX24.callMethod() должны делаться ТОЛЬКО внутри BX24.init().
 */
BX24.init(function () {
  // Логируем готовность SDK в консоль установщика — администратор видит прогресс.
  log('BX24.init — SDK готов');

  // ── Шаг 1: проверить, не установлено ли приложение уже ────────────────────
  // Зачем: если пользователь случайно повторно открыл страницу установки,
  // мы не должны создавать поля второй раз или перерегистрировать placement.
  // API-метод app.info возвращает информацию о текущем приложении, в том числе
  // поле INSTALLED: true/false — флаг завершённой установки (после installFinish).

  BX24.callMethod(
    'app.info',  // Метод REST API: возвращает данные о текущем приложении
    {},          // Параметры: пусто — метод не принимает фильтров
    function (infoRes) {
      // infoRes — объект ответа BX24 SDK.
      // infoRes.error() — возвращает объект ошибки или null при успехе.
      if (infoRes.error()) {
        // Если app.info вернул ошибку — показываем сообщение и прерываем установку.
        showError('Ошибка app.info: ' + infoRes.error());
        return; // Прерываем выполнение колбэка
      }

      // infoRes.data() — JavaScript-объект с данными ответа.
      // INSTALLED: true — Битрикс24 уже зафиксировал завершение установки (installFinish был вызван ранее).
      if (infoRes.data().INSTALLED) {
        // Повторная установка не нужна — сообщаем об этом и останавливаемся.
        showSuccess('Приложение уже установлено. Повторная установка не требуется.');
        setStep('Готово', 1, 1); // Прогресс-бар = 100%
        return; // Выходим из колбэка — дальнейшая цепочка не запускается
      }

      // INSTALLED = false → приложение устанавливается впервые.
      log('Статус: приложение ещё не установлено → запуск установки');
      // Переходим к следующему шагу — загрузке существующих UF-полей.
      loadExistingFields();
    }
  );
});

// ─── Шаг 2: получить существующие UF-поля лида ────────────────────────────────
// Зачем: перед созданием полей нужно убедиться, каких полей ещё нет.
// Это делает установку идемпотентной: если часть полей уже создана (например,
// установка прервалась на полпути), мы создадим только недостающие.

function loadExistingFields() {
  // Устанавливаем статус прогресса: загрузка (total=0 → счётчик скрыт).
  setStep('Загрузка существующих полей лида...', 0, 0);

  BX24.callMethod(
    'crm.lead.userfield.list', // Метод REST API: возвращает список всех UF-полей лида
    {
      order: { SORT: 'ASC' }   // Сортировка по возрастанию поля SORT (порядковый номер)
      // filter не указан → возвращаются все UF-поля лида, не только наши KC_*
    },
    function (listRes) {
      if (listRes.error()) {
        // Ошибка получения списка полей — останавливаем установку.
        showError('Ошибка crm.lead.userfield.list: ' + listRes.error());
        return;
      }

      // Строим хэш-таблицу (объект) существующих полей для быстрого поиска O(1).
      // Ключ — полное имя поля вида 'UF_CRM_KC_FULLNAME'.
      // Значение — true (просто флаг присутствия).
      var existing = {};
      // listRes.data() — массив объектов полей. Каждый объект содержит FIELD_NAME, ID, USER_TYPE_ID и т.д.
      var data = listRes.data() || []; // || [] — защита от null/undefined
      for (var i = 0; i < data.length; i++) {
        // data[i].FIELD_NAME уже содержит полный префикс «UF_CRM_».
        existing[data[i].FIELD_NAME] = true;
      }

      // Обработка пагинации Битрикс24 REST API.
      // По умолчанию API возвращает не более 50 записей на страницу.
      // listRes.more() === true → есть ещё страницы данных.
      // listRes.next() → автоматически инициирует следующий запрос с нужным offset.
      // Примечание: для простоты мы работаем только с первой страницей.
      // На практике количество UF-полей лида редко превышает 50, поэтому одна страница достаточна.
      if (listRes.more()) {
        listRes.next(); // Подгружаем следующую страницу (колбэк вызовется повторно)
        // Для простоты — работаем с тем, что получили за первую страницу.
        // На практике полей лида обычно < 50, поэтому достаточно одной страницы.
      }

      // Логируем общее число найденных UF-полей для диагностики.
      var existingCount = Object.keys(existing).length;
      log('Найдено существующих UF-полей лида: ' + existingCount);

      // Формируем массив только тех полей из UF_FIELDS, которых ещё нет в CRM.
      var toCreate = [];
      for (var j = 0; j < UF_FIELDS.length; j++) {
        // API добавляет префикс «UF_CRM_» к FIELD_NAME автоматически при создании.
        // Для сравнения с existing нам нужно вручную добавить этот префикс.
        var fullName = 'UF_CRM_' + UF_FIELDS[j].FIELD_NAME;
        if (existing[fullName]) {
          // Поле уже существует → пропускаем, сообщаем в лог.
          log('  ✓ ' + fullName + ' — уже существует, пропуск');
        } else {
          // Поля нет → добавляем в очередь на создание.
          toCreate.push(UF_FIELDS[j]);
        }
      }

      if (toCreate.length === 0) {
        // Все 27 полей уже созданы — переходим сразу к регистрации вкладки.
        log('Все 27 полей уже созданы — переход к регистрации вкладки');
        bindPlacement();
        return;
      }

      log('Нужно создать полей: ' + toCreate.length);
      // Запускаем цепочку последовательного создания полей с индекса 0.
      createFieldsSequentially(toCreate, 0);
    }
  );
}

// ─── Шаг 3: создание полей по цепочке ────────────────────────────────────────
// Последовательное (не параллельное) создание через рекурсивную цепочку колбэков.
//
// Зачем последовательно, а не параллельно (batch)?
//   — Надёжнее: нет риска превысить лимит параллельных запросов REST API Битрикс24.
//   — Прозрачнее: прогресс-бар обновляется после каждого созданного поля.
//   — Безопаснее: при ошибке точно знаем, на каком поле она произошла.

/**
 * createFieldsSequentially(fieldsToCreate, idx) — рекурсивная функция.
 * Создаёт поле с индексом idx, затем вызывает себя для idx+1.
 * Останавливается, когда idx >= fieldsToCreate.length.
 *
 * @param {Array}  fieldsToCreate — массив объектов полей из UF_FIELDS (только недостающие).
 * @param {number} idx            — текущий индекс обрабатываемого поля (0-based).
 */
function createFieldsSequentially(fieldsToCreate, idx) {
  // Базовый случай рекурсии: все поля обработаны → переходим к следующему шагу.
  if (idx >= fieldsToCreate.length) {
    log('Все поля созданы успешно');
    bindPlacement(); // Переходим к шагу 4: регистрация вкладки
    return;
  }

  var total    = fieldsToCreate.length;       // Всего полей для создания
  var field    = fieldsToCreate[idx];         // Текущий объект поля из UF_FIELDS
  var fullName = 'UF_CRM_' + field.FIELD_NAME; // Полное имя поля (для лога)

  // Обновляем прогресс-бар: +1 к текущему шагу, +1 в total (последний шаг = bindPlacement).
  setStep('Создание поля: ' + fullName, idx + 1, total + 1);

  // ── Формируем объект fields для передачи в crm.lead.userfield.add ──────────
  var apiFields = {
    FIELD_NAME:   field.FIELD_NAME,                 // Суффикс имени (без «UF_CRM_»)
    USER_TYPE_ID: field.USER_TYPE_ID,               // Тип данных: string, integer, enumeration, datetime
    LABEL:        field.LABEL,                      // Отображаемое имя поля
    MANDATORY:    field.MANDATORY || 'N',           // Обязательность ('Y'/'N'); по умолчанию 'N'
    SORT:         field.SORT || 100,                // Порядок сортировки; по умолчанию 100
    MULTIPLE:     'N',                              // Множественное значение — НЕТ (единственное значение)
    SHOW_FILTER:  'N',                              // Не показывать поле в фильтре списка лидов
    EDIT_IN_LIST: 'Y',                              // Разрешить редактирование прямо из списка лидов

    // EDIT_FORM_LABEL, LIST_COLUMN_LABEL, LIST_FILTER_LABEL — локализованные метки.
    // Объект с ключом 'ru' задаёт русскоязычную подпись поля в интерфейсе CRM.
    EDIT_FORM_LABEL:    { ru: field.LABEL },
    LIST_COLUMN_LABEL:  { ru: field.LABEL },
    LIST_FILTER_LABEL:  { ru: field.LABEL }
  };

  // SETTINGS — необязательный раздел, передаём только если задан в UF_FIELDS.
  // Для 'string' с ROWS:3 — textarea; для 'enumeration' — способ отображения.
  if (field.SETTINGS) {
    apiFields.SETTINGS = field.SETTINGS;
  }

  // LIST — массив вариантов для 'enumeration'. Передаём только для таких полей.
  // Каждый элемент: { VALUE, XML_ID, SORT }.
  if (field.LIST) {
    apiFields.LIST = field.LIST;
  }

  BX24.callMethod(
    'crm.lead.userfield.add', // Метод REST API: создаёт пользовательское поле лида
    {
      fields: apiFields         // Объект с описанием нового поля
    },
    function (addRes) {
      if (addRes.error()) {
        // Анализируем текст ошибки: некоторые ошибки не являются критическими.
        var errMsg = String(addRes.error());

        // Если поле уже существует (гонка состояний или повторный запуск) — не прерываем,
        // просто предупреждаем в логе. Ошибки «already exists» и «FIELD_NAME_DUPLICATED»
        // возвращаются разными версиями API Битрикс24.
        if (errMsg.indexOf('already exists') !== -1 || errMsg.indexOf('FIELD_NAME_DUPLICATED') !== -1) {
          log('  ⚠ ' + fullName + ' — уже существует (пропуск)');
        } else {
          // Критическая ошибка (нет прав, невалидные данные и т.д.) — прерываем установку.
          showError('Ошибка создания ' + fullName + ': ' + addRes.error());
          return; // Рекурсия не продолжается — установка завершается с ошибкой
        }
      } else {
        // addRes.data() — ID нового поля, присвоенный Битрикс24.
        var newId = addRes.data();
        log('  ✓ ' + fullName + ' — создано (ID: ' + newId + ')');
      }

      // Рекурсивно вызываем себя для следующего поля (idx + 1).
      // Это создаёт «цепочку колбэков», а не цикл — следующий запрос стартует
      // только после получения ответа на предыдущий.
      createFieldsSequentially(fieldsToCreate, idx + 1);
    }
  );
}

// ─── Шаг 4: регистрация вкладки CRM_LEAD_DETAIL_TAB ─────────────────────────
// Зачем: приложение должно быть видно в карточке лида как отдельная вкладка «Анкета».
// placement.bind регистрирует «placement» — точку встройки виджета в интерфейс Битрикс24.

function bindPlacement() {
  setStep('Регистрация вкладки в карточке лида...', 0, 0); // Прогресс без счётчика
  log('placement.bind → CRM_LEAD_DETAIL_TAB, handler: ' + HANDLER_URL);

  BX24.callMethod(
    'placement.bind',          // Метод REST API: регистрирует встройку приложения
    {
      PLACEMENT: 'CRM_LEAD_DETAIL_TAB', // Точка встройки: вкладка в карточке лида CRM
      HANDLER:   HANDLER_URL,           // URL обработчика — index.php, загружаемый во вкладке
      TITLE:     'Анкета',              // Название вкладки по умолчанию (для браузеров без локализации)
      LANG_ALL: {
        ru: { TITLE: 'Анкета' },        // Русскоязычное название вкладки
        en: { TITLE: 'Questionnaire' }  // Англоязычное название вкладки
      }
    },
    function (bindRes) {
      if (bindRes.error()) {
        // Ошибка регистрации вкладки — критическая: приложение будет недоступно из CRM.
        showError('Ошибка placement.bind: ' + bindRes.error());
        return;
      }
      log('  ✓ Вкладка «Анкета» зарегистрирована');
      // Переходим к последнему шагу — завершению установки.
      finishInstall();
    }
  );
}

// ─── Шаг 5: завершение установки ─────────────────────────────────────────────
// Финальный шаг: сигнализируем Битрикс24, что установка завершена.

function finishInstall() {
  // Устанавливаем прогресс-бар в 100% (current=1, total=1).
  setStep('Готово', 1, 1);

  // Показываем итоговое сообщение в зелёном блоке.
  showSuccess(
    'Установка завершена. ' +
    'Создано ' + UF_FIELDS.length + ' полей, вкладка «Анкета» зарегистрирована в карточке лида.'
  );
  log('BX24.installFinish() — сигнализируем Битрикс24 о завершении');

  /**
   * BX24.installFinish() — специальный метод SDK, сигнализирующий платформе
   * о завершении процесса установки приложения.
   *
   * После его вызова происходит следующее:
   *   1. Битрикс24 устанавливает флаг INSTALLED = true для приложения.
   *   2. Встройки (placement'ы) приложения становятся видны всем пользователям портала.
   *   3. Обработчики событий приложения активируются.
   *   4. Страница install.php автоматически перезагружается и открывается
   *      стартовый экран приложения (если задан в настройках приложения).
   *
   * ВАЖНО: без вызова installFinish() Битрикс24 будет показывать всем пользователям
   * сообщение «Приложение ещё не установлено» вместо самого приложения.
   */
  BX24.installFinish();
}
