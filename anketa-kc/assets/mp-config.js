/**
 * getActiveMPs()
 * Возвращает массив объектов всех МП, у которых active === true.
 * Используется в форме для построения списка доступных менеджеров.
 *
 * @returns {Array<Object>} — массив конфигурационных объектов активных МП
 */
function getActiveMPs() {
  return Object.values(MP_CONFIG).filter(mp => mp.active);
}

/**
 * getMPTimezone(mpId)
 * Возвращает UTC-смещение (в часах) для МП с указанным ID.
 * Смещение берётся из справочника CITIES_TZ (cities.js) по полю mp.city.
 * Если getCityTZ не загружена (cities.js не подключён) — возвращает null.
 *
 * @param {number|string} mpId — ключ МП в MP_CONFIG
 * @returns {number|null} — UTC-офсет в часах (например, 3 для Москвы) или null
 */
function getMPTimezone(mpId) {
  const mp = MP_CONFIG[mpId];
  if (!mp) return null;
  // getCityTZ определена в cities.js; проверяем наличие перед вызовом
  return (typeof getCityTZ === 'function') ? getCityTZ(mp.city) : null;
}

/**
 * isMPWorkday(mpId, date)
 * Проверяет, является ли переданная дата рабочим днём для данного МП.
 * Сравнивает день недели объекта Date с массивом workDays МП.
 *
 * @param {number|string} mpId — ключ МП в MP_CONFIG
 * @param {Date} date          — объект Date для проверки
 * @returns {boolean} — true, если день рабочий и МП активен; иначе false
 */
function isMPWorkday(mpId, date) {
  const mp = MP_CONFIG[mpId];
  if (!mp || !mp.active) return false;
  // date.getDay() возвращает 0 (Вс) … 6 (Сб)
  return mp.workDays.includes(date.getDay());
}

/**
 * getMPDaySlots(mpId, localDate)
 * Вычисляет список временных слотов для МП на заданный день.
 * Каждый слот содержит:
 *   - localTime  — время начала слота в локальном времени МП ("HH:MM")
 *   - utcTime    — то же время, пересчитанное в UTC ("HH:MM")
 *   - isoStart   — ISO 8601 строка начала слота (используется при записи)
 *
 * Алгоритм:
 *   1. Переводит workStart и workEnd из "HH:MM" в общее количество минут.
 *   2. Перебирает минуты с шагом slotMinutes от workStart до workEnd.
 *   3. Для каждого шага вычитает UTC-офсет МП, чтобы получить UTC-время.
 *   4. Корректирует выход за границы суток через остаток от 1440 (минут/сутки).
 *
 * @param {number|string} mpId    — ключ МП в MP_CONFIG
 * @param {Date}          localDate — дата, для которой строятся слоты
 * @returns {Array<{localTime: string, utcTime: string, isoStart: string}>}
 *          — массив слотов или [] если МП неактивен / город не найден в CITIES_TZ
 */
function getMPDaySlots(mpId, localDate) {
  const mp = MP_CONFIG[mpId];
  if (!mp || !mp.active) return [];

  // Получаем UTC-смещение из CITIES_TZ по городу МП
  const tzOffset = getMPTimezone(mpId);
  if (tzOffset === null) return []; // Город не найден в справочнике

  const slots = [];

  // Разбираем время начала и конца рабочего дня
  const [startH, startM] = mp.workStart.split(':').map(Number);
  const [endH, endM]     = mp.workEnd.split(':').map(Number);

  // Переводим в минуты от начала суток для удобства арифметики
  const startMinutes = startH * 60 + startM;
  const endMinutes   = endH   * 60 + endM;

  // Генерируем слоты с шагом slotMinutes
  for (let m = startMinutes; m < endMinutes; m += mp.slotMinutes) {
    // Форматируем локальное время слота "HH:MM"
    const hh = String(Math.floor(m / 60)).padStart(2, '0');
    const mm = String(m % 60).padStart(2, '0');
    const localTime = `${hh}:${mm}`;

    // Пересчитываем в UTC: вычитаем смещение часового пояса (в минутах)
    const utcM = m - tzOffset * 60;
    // Нормализуем в диапазон [0, 1439]: ((x % 1440) + 1440) % 1440
    // защищает от отрицательных значений при UTC-смещении > workStart
    const utcTotal = ((utcM % 1440) + 1440) % 1440;
    const utcH  = String(Math.floor(utcTotal / 60)).padStart(2, '0');
    const utcMm = String(utcTotal % 60).padStart(2, '0');
    const utcTime = `${utcH}:${utcMm}`;

    // Формируем ISO-строку начала слота на основе переданной даты
    const slotDate = new Date(localDate);
    slotDate.setUTCHours(Math.floor(utcTotal / 60), utcTotal % 60, 0, 0);
    const isoStart = slotDate.toISOString();

    slots.push({ localTime, utcTime, isoStart });
  }
  return slots;
}

