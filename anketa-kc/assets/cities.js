// Вспомогательная функция: получить UTC-офсет по названию города (или null)
function getCityTZ(cityName) {
  if (!cityName) return null;
  const name = cityName.trim();
  if (CITIES_TZ[name] !== undefined) return CITIES_TZ[name];
  // Нечёткий поиск: ищем ключ, содержащий введённое название
  const lower = name.toLowerCase();
  const found = Object.keys(CITIES_TZ).find(k => k.toLowerCase().includes(lower));
  return found ? CITIES_TZ[found] : null;
}

if (typeof module !== 'undefined') module.exports = { CITIES_TZ, getCityTZ };
